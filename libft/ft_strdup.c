#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	char	*start;

	if ((dest = (char*)malloc((ft_strlen(src) + 1) * sizeof(char))) == NULL)
		return (NULL);
	start = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (start);
}
